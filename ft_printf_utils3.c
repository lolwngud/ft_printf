#include "ft_printf.h"

int	ft_putstr_fd(char *s, int fd)
{
	int	i;

	i = 0;
	if (s && fd >= 0)
	{
		while (s[i] != 0)
			write(1, &s[i++], 1);
	}
	return (i);
}
